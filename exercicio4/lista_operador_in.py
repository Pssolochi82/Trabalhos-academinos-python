# Crie uma lista de números inteiros e utilize o operador in para verificar se o número 7 está presente.

numeros = [1, 3, 5, 7, 9, 11]
if 7 in numeros:
    print("O número 7 está presente na lista.")
else:
    print("O número 7 não está presente na lista.")
# Saída esperada: O número 7 está presente na lista.
